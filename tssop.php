          <h4 id="tsservicecall" class="boldtextsop">Service Call / TS Visit Report</h4>
          <p>
          <ul>
            <li>Always get TS number from 0800-10203 BEFORE responding to the service call.</li>
            <li>Service call has higher priority; you must report / respond to service call as early as possible. </li>
            <li>On completion of Service call, fill TS Visit Report and submit the copy on line.</li>
            <li>Send SMS to TSS when you reach at customer site and when service call is solved before leaving the customer site.</li>
            <li>It is responsibility of TS Person to keep updating TSS and SAP on progress of any service call at each step. SAP may follow up with TSS for the early solution of any Technical Problem at their assigned customer sites. </li>
            <li>On EVERY Service Call you MUST write online Troubleshooting Details.</li>
          </ul>
          </p>
          <h4 id="tstraining" class="boldtextsop">Training (Customers and SAP)</h4>
          <p>
          <ul>
            <li>TS Person will be responsible for Trainings of Customers as well as SAP:
              <ul>
                <li>Customers at New installations or previous installations but new operators or refresher course to old operators.</li>
                <li>SAP on Instruments and Reagents for Routine Operation and Maintenance, explaining them selling features of the instrument and benefit of each feature.</li>
              </ul>
            </li>
            <li>At the end of each training session “Instrument Training Completion Form” will be filled and signed / Stamped by customer or SAP.</li>
          </ul>
          </p>
          </p>
          <h4 id="iif" class="boldtextsop">Instrument Installation Form (IIF)</h4>
          <p>
          <ul>
            <li>DO NOT INSTALL the instrument if NO PROPER GROUND / EARTH connection is available.</li>
            <li>Whenever a new instrument is Imported / installed / Shifted from one customer to another. TS Person will fill the Instrument Installation Form (IIF) and will note the details of the instrument serial number, Date and time of installation on the IIF. </li>
            <li>TS Person will paste on the instrument 0800 Sticker and no personal cards.</li>
            <li>TS Person will get signature from customer and TSS will send IIF to STCO for keeping record in the serial number file of the Equipment.</li>
            <li>All Beckman Instrument Installation Report will be prepared at the time of First installation and send to Director Sales for Further sending it to BC immediately at the time of installation (This report does not require customer signature).</li>
          </ul>
          </p>
          <h4 id="pm" class="boldtextsop">Instrument Preventive Maintenance (PM)</h4>
          <p>
          <ul>
            <li>Each TS Person will be assigned certain Instruments in his territory to perform PM of these instruments. </li>
            <li>Perform PM as per PM Schedule on assigned week but remember that Service call has higher priority than PM.</li>
            <li>Always schedule your visit with the customer, take a time and day so that PM should not disturb their routine work.</li>
            <li>If a PM visit schedule is disturbed because of an urgent call from another customer. Inform customer with apology about change of program and reschedule visit as soon as possible.</li>
            <li>If any instrument is not functional in your territory and it’s been assigned to you. It’s your responsibility to inform your TSS about its status via email and copy to TSA so that it is removed from your list of assigned instruments. </li>
            <li>All TS Persons will DIRECTLY get TS Number before going for the PM or service call.</li>
            <li>TSA will not issue TS Number if the PM has already been performed.</li>
            <li>Submit the PM Form after getting customer signature to TSS on the same day with all attachments.</li>
          </ul>
          </p>
          <h4 id="om" class="boldtextsop">Operator Maintenance (OM) </h4>
          <p>
          <ul>
            <li>It is the responsibility of TS Persons to train the operators on how to perform Operator Level Maintenance (Daily, Weekly and Monthly) of the Instrument.</li>
            <li>TS Person is responsible to ensure customer does the maintenance it is TS Persons responsibility to ensure that </li>
            <li>It is the responsibility of TS Person to ensure that Every Instrument at his assigned customer site must have Operator Maintenance sheet and it is properly maintained.</li>
            <li>At the end of each month TS Person will collect OMR of last month for all instruments assigned to him. A copy of the same will be delivered to TSS, TSA and Manager SAP.</li>
            <li>All Operator Maintenance sheets must be signed by Lab Manager / Supervisor, Pathologist, Head of Pathology Department / Chief pathologist.</li>
          </ul>
          </p>
          <h4 id="replaceparts" class="boldtextsop">Replaced Parts and Warranties </h4>
          <p>
          <ul>
            <li>TS Person cannot replace any Part without PRIOR Online Approval of Parts Requisition Form.</li>
            <li>Always label all spare parts with the corresponding serial number of Instrument with date.</li>
            <li>Assigned TS person will Draft email of Warranty Claim against the parts replaced by him and send to TSS and Director.</li>
            <li>Parts will be issued on DC in the name of TS Person and TSS. One copy of DC will be for the Office File, TSS and TS Person (Receiving copy, which will be send back to TS Coordinator).</li>
            <li>TS person will mention the detail of the parts actually replaced on the TS Report.</li>
            <li>All Unused or Replaced or Faulty parts and Receiving copy of the DC must be sent back to STCO / TSA within the 7 days of the date issue of DC / parts.</li>
          </ul>
          </p>
